package lab4;

public class Person {
    private String nume;
    private int age;
    private String email;

    public void setNume(String nume) {
        this.nume = nume;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNume() {
        return nume;
    }

    public int getAge() {
        return age;
    }

    public String getEmail() {
        return email;
    }
}
